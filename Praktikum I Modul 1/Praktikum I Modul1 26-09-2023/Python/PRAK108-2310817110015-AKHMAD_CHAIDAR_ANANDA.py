PI = 3.14
putaran = 5
jarak_tempuh = 14

keliling = jarak_tempuh / putaran
r = keliling / (2 * PI)

print("Diketahui :")
print("Pak Dengklek mengelilingi taman =",putaran)
print("Jarak tempuh Pak Dengklek =",jarak_tempuh,'\n')

print("Jawaban :")
print("Jari-jari taman yang dikelilingi Pak Dengklek adalah",format(r, '.2f'))